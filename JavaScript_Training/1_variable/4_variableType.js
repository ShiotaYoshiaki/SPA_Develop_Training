/**
 * typeof → 変数に格納されている値の型を判定できる
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Operators/typeof
 */

console.log('====================');

const sampleNumber = 7;
console.log(sampleNumber);
console.log(typeof sampleNumber);

console.log('====================');

const sampleString = 'sample';
console.log(sampleString);
console.log(typeof sampleString);

console.log('====================');

const sampleBool = false;
console.log(sampleBool);
console.log(typeof sampleBool);

console.log('====================');

const dummy = undefined;
console.log(dummy);
console.log(typeof dummy);
